import unittest

import numpy as np

import expfit


class TestFitd2EC1(unittest.TestCase):
    """
    Tests fitting of double exponentials.
    """
    def test_fitd2_repeat(self):

        plot = False
        p0 = -87, 30, .34, 40, .05
        t = np.linspace(0, 2, 200)

        p = expfit.fitd2(
            t, v, plot=p0 if plot else False, opt_plot=plot)
        if plot:  # pragma: no cover
            import matplotlib.pyplot as plt
            plt.show()

        rt = expfit.rmse(t, v, p0)
        rf = expfit.rmse(t, v, p)
        self.assertLess(rf / rt, 1)


